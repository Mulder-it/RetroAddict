
<h1>Bienvenue sur RetroAddict</h1>

<p>RetroAddict est un site web pour la amateur de rétro-gaming</p>

<p>Un aspect non négligeable du retrogaming concerne la collection de consoles, de bornes d'arcade et de jeux vidéo anciens.
    En effet, de nombreux adeptes de jeux rétro fréquentent assidûment les brocantes, vide-greniers, marchés aux puces,
    boutiques de troc et sites de ventes aux enchères de particulier à particulier, afin de retrouver les machines ou les
    jeux manquants à leur collection, et avec eux les graphismes et la maniabilité originales que ne peut apporter parfaitement
    l'émulation sur des ordinateurs ou des consoles modernes</p>

<p>Vous trouverez sur ce site des articles sur pour en savoir plus sur le monde du Rétro gaming et sur les moyens d'y adhérer
ou de l'aide pour restaurer votre ancien matériel gaming</p>